# 模块级常量（全大写命名约定）
PI: float = 3.14159265358979
VERSION: str = "1.0.0"


# ============================================================
# 公开函数（供外部导入使用）
# ============================================================
def is_prime(n: int) -> bool:
    """判断一个数是否为素数"""
    if n < 2:
        return False
    for i in range(2, int(n ** 0.5) + 1):
        if n % i == 0:
            return False
    return True


def factorial(n: int) -> int:
    """计算阶乘 n!"""
    if n < 0:
        raise ValueError("负数没有阶乘")
    result: int = 1
    for i in range(2, n + 1):
        result *= i
    return result


def gcd(a: int, b: int) -> int:
    """计算最大公约数（辗转相除法）"""
    while b:
        a, b = b, a % b
    return abs(a)


